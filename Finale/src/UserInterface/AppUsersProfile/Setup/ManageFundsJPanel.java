    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        if (!userAccount.getInvestor().getFinancialsDirectory().getFinancialsList().isEmpty()) {
            ArrayList<String> finList = new ArrayList<>();
            for (Financials fin : userAccount.getInvestor().getFinancialsDirectory().getFinancialsList()) {
                finList.add(fin.getSource());
            }
            if (finList.contains(sourceNameComboBox.getSelectedItem())) {
                JOptionPane.showMessageDialog(this, "Source is already present", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        Financials financials = userAccount.getInvestor().getFinancialsDirectory().addFinancials();
        financials.setSource((String) sourceNameComboBox.getSelectedItem());
        financials.setModeofSource((String) modeOfSourceComboBox.getSelectedItem());
        financials.setIsAutoSync(true);
        financials.setLastUpdated(new Date());
        populateTable();
    }//GEN-LAST:event_submitJButtonActionPerformed

    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        FinanceWorkAreaJPanel financeWorkAreaJPanel = (FinanceWorkAreaJPanel) component;
        financeWorkAreaJPanel.populateTable();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backJButtonActionPerformed

