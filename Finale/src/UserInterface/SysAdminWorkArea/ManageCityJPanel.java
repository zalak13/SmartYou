    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        Network network = (Network) stateNameJComboBox.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) LocationTypeJComboBox.getSelectedItem();
        String cityName = nameJTextField.getText().replaceAll("^\\s+|\\s+$", "");

        if (network == null || type == null || cityName == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }
        ArrayList<String> appList = new ArrayList<>();
        for (int count = 0; count < enterpriseJTable.getRowCount(); count++) {
            appList.add(enterpriseJTable.getValueAt(count, 0).toString());
        }
        if (appList.contains(cityName)) {
            JOptionPane.showMessageDialog(this, "City is already added.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(cityName, type);
        populateTable();
    }//GEN-LAST:event_submitJButtonActionPerformed

