    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                Object[] row = new Object[3];
                row[0] = enterprise.getName();
                row[1] = network.getName();
                row[2] = enterprise.getEnterpriseType().getValue();

                model.addRow(row);
            }
        }
    }

    private void populateComboBox() {
        stateNameJComboBox.removeAllItems();
        LocationTypeJComboBox.removeAllItems();

        for (Network network : system.getNetworkList()) {
            stateNameJComboBox.addItem(network);
        }

        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            LocationTypeJComboBox.addItem(type);
        }
    }
    
public void restrictAlphabetinTxtFields(java.awt.event.KeyEvent evt) {
     char c = evt.getKeyChar();
     JTextField jt = (JTextField) evt.getSource();
     if (!((c >= 'A') && (c <= 'Z') || (c >= 'a') && (c <= 'z')
             || (c == KeyEvent.VK_BACK_SPACE)
             || (c == KeyEvent.VK_DELETE)
             || (c == KeyEvent.VK_SPACE))) {
         getToolkit().beep();
         jt.setText("");
         jt.setBackground(Color.red);
         evt.consume();

     } else {
         jt.setBackground(Color.white);
     }
 }

